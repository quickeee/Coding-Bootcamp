CREATE TABLE `bootcamp`.`courses` (
  `course id` INT NOT NULL,
  `name` VARCHAR(45) NULL,
  `name of the lecturer` VARCHAR(45) NULL,
  `name of the assistant` VARCHAR(45) NULL,
  `duration of the course` INT NULL,
  `starting date of the course` DATE NULL,
  `coursescol` VARCHAR(45) NULL,
  PRIMARY KEY (`course id`));